    public void GenerarProductos(String pNombreCategoria){
        
         int fila=2,columna=4;
           Border blackline;       
        blackline = BorderFactory.createLineBorder(Color.black);
         
        if(ContarProductosporCategoria(arrayProductos,pNombreCategoria)>8){
            fila=3;        
        }
        
          panelProductos.setLayout(new GridLayout(fila,columna));          
          panelProductos.setSize(1200,700);
          panelProductos.setLocation(50, 50);
          
          
          
       
         
            for(int i= 0 ; i< arrayProductos.size();i++){
                if(arrayProductos.get(i).getCategoriaProducto().getNombreCat().equals(pNombreCategoria)){
                    JLayeredPane layered = new JLayeredPane();
                    layered.setBorder(blackline);
                    layered.setSize(300,300);
                                        
                   
                    
                    JLabel codigoProducto = new JLabel(String.valueOf(arrayProductos.get(i).getCodProducto()));
                    codigoProducto.setVisible(false);
                    
                    JLabel Banner = new JLabel(arrayProductos.get(i).getBanner());
                    Banner.setBorder(blackline);
                    Banner.setSize(200, 20);
                    Banner.setOpaque(true);
                    Banner.setBackground(Color.green);
                    Banner.setLocation(0, 0);
                    
                    ImageIcon imgProducto = new ImageIcon(arrayProductos.get(i).getUrlImagen());
                    JLabel ImgLabel = new JLabel();
                    ImgLabel.setIcon(imgProducto);                    
                    ImgLabel.setSize(240, 160);                    
                    ImgLabel.setLocation(0, 30);
                    
                    
                    JLabel TituloPro = new JLabel(arrayProductos.get(i).getTituloPro());
                    TituloPro.setSize(280, 40);
                    TituloPro.setLocation(20, 180);
                    
                    
                    JLabel Descripcion = new JLabel(arrayProductos.get(i).getDescripcion());
                    Descripcion.setSize(280, 40);
                    Descripcion.setLocation(20, 200);
                    
                      
                    JLabel Marca = new JLabel(arrayProductos.get(i).getMarca());
                    Marca.setSize(280, 40);
                    Marca.setLocation(20, 220);
                    
                    JLabel Precio = new JLabel( "S./" + String.valueOf(arrayProductos.get(i).getPrecio()));
                    Precio.setSize(100, 40);
                    Precio.setLocation(250, 250);
                    
                    JCheckBox ChkSeleccion = new JCheckBox();
                    ChkSeleccion.setSize(20, 20);
                    ChkSeleccion.setLocation(280, 10);
                    if(arrayProductos.get(i).isDisponibilidad() == false){
                        ChkSeleccion.enable(false); 
                        Precio.setText("Agotado");
                    }
                    
                   
                    if(fila==3){
                         Banner.setLocation(0, 0);
                        ImgLabel.setLocation(0, 20);
                        TituloPro.setLocation(20, 180);
                         ChkSeleccion.setLocation(320, 10);
                         Marca.setLocation(20, 200);
                         Descripcion.setLocation(20, 190);
                         
                         Precio.setLocation(250, 200);
                    
                    }
                  
                    layered.add(codigoProducto);
                    layered.add(Banner);
                    layered.add(ImgLabel);
                    layered.add(ChkSeleccion);
                    layered.add(TituloPro);
                    layered.add(Descripcion);
                    layered.add(Marca);
                    layered.add(Precio);
                    
                    
                     ChkSeleccion.addActionListener(new ActionListener() {
                    public void actionPerformed(ActionEvent e) {                            
                            JLabel codPro = (JLabel)layered.getComponent(0);
                            
                             if(ChkSeleccion.isSelected() == true){                                                           
                                 arrayProductosSelecionados.add(ObtenerProductoOfProductos( arrayProductos, Integer.parseInt(codPro.getText())));
                            
                            }else{
                             
                                 arrayProductosSelecionados.remove(ObtenerProductoOfProductos( arrayProductos, Integer.parseInt(codPro.getText())));
                             }
                            
                            
                            
                        }	
                    });
                    
                   panelProductos.add(layered);
                   jDialogRealizarPedidoProductos.add(panelProductos);
                            
                    
                }
            
           }
            
        
    }
    
