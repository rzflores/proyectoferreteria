    private void jBtnResumenFinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnResumenFinalActionPerformed
        FileWriter fw;
        String texto;
        String nuevalinea = System.getProperty("line.separator");
        pedido = new Pedido(arrayPedidos.size(),
                TxtFechaPedido.getText(),
                TxtFechaEntrega.getText(),
                Double.parseDouble(TxtSubtotal.getText()),
                Double.parseDouble(TxtIGV.getText()),
                Double.parseDouble(TxtTotal.getText()),
                TxtNombreCliente.getText(),
                TxtCorreoCliente.getText(),
                TxtDistritoCliente.getText(),
                TxtCelularCliente.getText(),
                TxtDireccionCliente.getText(),
                arrayProductosSelecionados
        );
        arrayPedidos.add(pedido);
        texto = "NroPedido:" + arrayPedidos.size() + nuevalinea +
                "Fecha Pedido:" + TxtFechaPedido.getText()+ nuevalinea +
                "Fecha de Entrega:"+ TxtFechaEntrega.getText()+ nuevalinea +
                "Subtotal:" + Double.parseDouble(TxtSubtotal.getText())+ nuevalinea +
                "IGV:"+Double.parseDouble(TxtIGV.getText())+ nuevalinea +
                "Total:"+Double.parseDouble(TxtTotal.getText())+ nuevalinea +
                "Nombre:"+TxtNombreCliente.getText()+ nuevalinea +
                "Correo:"+TxtCorreoCliente.getText()+ nuevalinea +
                "Distrito:"+TxtDistritoCliente.getText()+ nuevalinea +
                "Celular:"+TxtCelularCliente.getText()+ nuevalinea +
                "Direccion:"+TxtDireccionCliente.getText();
        
        for(int i = 0 ; i < arrayProductosSelecionados.size();i++){
        texto += "Codigo Producto:" + arrayProductosSelecionados.get(i).getCodProducto() + nuevalinea +
                "Titulo:" + arrayProductosSelecionados.get(i).getTituloPro() + nuevalinea +
                 "Categoria:" + arrayProductosSelecionados.get(i).getCategoriaProducto().getNombreCat() + nuevalinea +
                 "Precio:" + arrayProductosSelecionados.get(i).getPrecio();
        }
        try{
        fw= new FileWriter("Resumen_nro_pedido" + arrayPedidos.size());
        fw.write(texto);
        fw.close();
          jOptionPane1.showMessageDialog( null, "Grabado con Exito" );
        }
        catch(IOException io){
              jOptionPane1.showMessageDialog( null, "Error al grabar el Resumen" );
        }
            
    }//GEN-LAST:event_jBtnResumenFinalActionPerformed

